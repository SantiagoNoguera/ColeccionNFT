let provider, signer, contract;

// Función para conectar con MetaMask
async function connectWallet() {
    if (window.ethereum) {
        try {
            provider = new ethers.providers.Web3Provider(window.ethereum);
            await provider.send("eth_requestAccounts", []);
            signer = provider.getSigner();
            contract = new ethers.Contract(contractAddress, contractABI, signer);
            
            // Mostrar la dirección de la billetera conectada
            const address = await signer.getAddress();
            document.getElementById('walletAddress').textContent = `Conectado a: ${address}`;
            
            document.getElementById('connectButton').textContent = 'Conectado';
            document.getElementById('actionsContainer').classList.remove('hidden'); // Mostrar las acciones
            alert('Conectado a MetaMask');
        } catch (error) {
            console.error('Error al conectar con MetaMask:', error);
            alert('Error al conectar con MetaMask');
        }
    } else {
        alert('MetaMask no está instalado');
    }
}

// Función para crear un NFT
async function createNFT() {
    const uri = document.getElementById('nftUri').value;
    if (!uri) {
        alert('Por favor ingresa un URI válido');
        return;
    }
    try {
        const tx = await contract.crearNFT(await signer.getAddress(), uri);
        await tx.wait();
        alert(`NFT creado con éxito. Tx Hash: ${tx.hash}`);
    } catch (error) {
        console.error('Error al crear NFT:', error);
        alert('Error al crear NFT');
    }
}

// Función para obtener NFTs del usuario
async function getNFTs() {
    try {
        const nfts = await contract.getNFTUsuario(await signer.getAddress());
        const nftList = document.getElementById('nftList');
        nftList.innerHTML = '';
        for (const tokenId of nfts) {
            const tokenUri = await contract.tokenURI(tokenId);
            const nftCard = document.createElement('div');
            nftCard.className = 'nft-card bg-white p-4 rounded shadow-md';
            nftCard.innerHTML = `
                <h2 class="text-lg font-semibold">NFT ID: ${tokenId}</h2>
                <img src="${tokenUri}" alt="NFT Image" class="w-full h-40 object-cover rounded mt-2">
                <p class="text-gray-600 mt-2">URI: ${tokenUri}</p>
            `;
            nftList.appendChild(nftCard);
        }
    } catch (error) {
        console.error('Error al obtener NFTs:', error);
        alert('Error al obtener NFTs');
    }
}

// Función para transferir un NFT
async function transferNFT() {
    const tokenId = document.getElementById('transferTokenId').value;
    const toAddress = document.getElementById('transferAddress').value;
    if (!tokenId || !toAddress) {
        alert('Por favor ingresa el ID del NFT y la dirección de destino');
        return;
    }
    try {
        const tx = await contract['safeTransferFrom(address,address,uint256)'](
            await signer.getAddress(),
            toAddress,
            tokenId
        );
        await tx.wait();
        alert(`NFT transferido con éxito. Tx Hash: ${tx.hash}`);
    } catch (error) {
        console.error('Error al transferir NFT:', error);
        alert('Error al transferir NFT');
    }
}

// Event Listeners
document.getElementById('connectButton').addEventListener('click', connectWallet);
document.getElementById('createNFTButton').addEventListener('click', createNFT);
document.getElementById('getNFTsButton').addEventListener('click', getNFTs);
document.getElementById('transferNFTButton').addEventListener('click', transferNFT);
